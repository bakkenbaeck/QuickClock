import Foundation
import UIKit

extension UIColor {
    
    open class var quickClockWhite: UIColor {
        return UIColor.white
    }
    
    open class var quickClockGray: UIColor {
        return UIColor(red: 243/255, green: 243/255, blue: 243/255, alpha: 1)
    }
    
    open class var quickClockDarkGray: UIColor {
        return UIColor(red: 30/255, green: 30/255, blue: 30/255, alpha: 1)
    }
    
    open class var quickClockGreen: UIColor {
        return UIColor(red: 87/255, green: 190/255, blue: 76/255, alpha: 1)
    }
}
